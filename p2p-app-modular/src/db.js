// PostgreSQL data layer.
//
// Exposes a better-sqlite3-like API so call sites stay uniform, but async:
//   await db.prepare(sql).get(...params)   → first row or undefined
//   await db.prepare(sql).all(...params)   → rows
//   await db.prepare(sql).run(...params)   → { lastInsertRowid, changes }
//     (INSERTs automatically get RETURNING id)
//   await db.tx(async () => { ... })       → all db calls inside run on one
//     transaction client (AsyncLocalStorage), committed/rolled back atomically.
//
// SQL uses `?` placeholders (converted to $n). Dates the user enters are TEXT
// 'YYYY-MM-DD'; timestamps are TEXT 'YYYY-MM-DD HH24:MI:SS' via to_char(now())
// to stay byte-compatible with what the frontend already renders.
//
// Config: DATABASE_URL (default postgres://p2p:p2p@localhost:5433/p2p_mod)
//         P2P_DATA_DIR for uploaded files (default ../data)
const path = require('path');
const fs = require('fs');
const { Pool, types } = require('pg');
const { AsyncLocalStorage } = require('async_hooks');

const DATA_DIR = process.env.P2P_DATA_DIR || path.join(__dirname, '..', 'data');
fs.mkdirSync(DATA_DIR, { recursive: true });

const DATABASE_URL = process.env.DATABASE_URL || 'postgres://p2p:p2p@localhost:5433/p2p_mod';

// COUNT()/SUM() come back as strings by default — return numbers like SQLite did
types.setTypeParser(20, (v) => parseInt(v, 10));      // int8
types.setTypeParser(1700, (v) => parseFloat(v));      // numeric

const pool = new Pool({ connectionString: DATABASE_URL, max: 10 });
pool.on('error', (err) => console.error('Postgres pool error:', err.message));

const als = new AsyncLocalStorage();
const conn = () => als.getStore() || pool;

const convert = (sql) => {
  let i = 0;
  return sql.replace(/\?/g, () => '$' + (++i));
};

function prepare(sql) {
  const text = convert(sql);
  return {
    async get(...params) {
      const r = await conn().query(text, params);
      return r.rows[0];
    },
    async all(...params) {
      const r = await conn().query(text, params);
      return r.rows;
    },
    async run(...params) {
      let t = text;
      if (/^\s*insert\b/i.test(t) && !/\breturning\b/i.test(t)) t += ' RETURNING id';
      const r = await conn().query(t, params);
      return { lastInsertRowid: r.rows && r.rows[0] ? r.rows[0].id : undefined, changes: r.rowCount };
    },
  };
}

async function tx(fn) {
  if (als.getStore()) return fn(); // already inside a transaction — join it
  const client = await pool.connect();
  try {
    await client.query('BEGIN');
    const out = await als.run(client, fn);
    await client.query('COMMIT');
    return out;
  } catch (e) {
    try { await client.query('ROLLBACK'); } catch { /* connection gone */ }
    throw e;
  } finally {
    client.release();
  }
}

const exec = (sql) => pool.query(sql);

const db = { prepare, tx, exec };

